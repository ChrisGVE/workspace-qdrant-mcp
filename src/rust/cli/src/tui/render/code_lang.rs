// ─── Comment prefix for each language ────────────────────────────────────────

/// The single-line comment prefix for a language, if any.
pub fn comment_prefix(language: &str) -> Option<&'static str> {
    match language {
        "rust" | "javascript" | "typescript" | "jsx" | "tsx" | "go" | "c" | "cpp" | "java"
        | "swift" | "kotlin" | "csharp" => Some("//"),
        "python" | "ruby" | "shell" | "toml" | "yaml" | "yml" => Some("#"),
        "lua" => Some("--"),
        "sql" => Some("--"),
        _ => None,
    }
}
