//! Language metadata for the code syntax highlighter.
//!
//! `src/rust/cli/src/tui/render/code_lang.rs`
//!
//! Contains the extension → language name map, per-language keyword sets, and
//! comment-prefix table. Split from `code.rs` to keep both files under the
//! 500-line limit.

// ─── Extension → language name ───────────────────────────────────────────────

/// Map a file extension (lowercase, without leading dot) to a canonical
/// language name used by `highlight_line`.
///
/// Returns `None` for unknown extensions; callers fall back to plain rendering.
pub fn language_for_extension(ext: &str) -> Option<&'static str> {
    match ext {
        "rs" => Some("rust"),
        "py" | "pyw" => Some("python"),
        "js" | "mjs" | "cjs" => Some("javascript"),
        "ts" | "mts" | "cts" => Some("typescript"),
        "jsx" => Some("jsx"),
        "tsx" => Some("tsx"),
        "go" => Some("go"),
        "c" | "h" => Some("c"),
        "cpp" | "cc" | "cxx" | "hpp" | "hh" | "hxx" => Some("cpp"),
        "java" => Some("java"),
        "rb" => Some("ruby"),
        "sh" | "bash" | "zsh" | "ksh" => Some("shell"),
        "json" => Some("json"),
        "toml" => Some("toml"),
        "yaml" | "yml" => Some("yaml"),
        "sql" => Some("sql"),
        "lua" => Some("lua"),
        "swift" => Some("swift"),
        "kt" | "kts" => Some("kotlin"),
        "cs" => Some("csharp"),
        _ => None,
    }
}

