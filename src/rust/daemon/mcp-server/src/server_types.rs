/// Default HTTP listener bind address.
///
/// Matches `DEFAULT_HTTP_HOST` in `server-types.ts` line 106.
/// Bind to `0.0.0.0` only inside a container.
pub const DEFAULT_HTTP_HOST: &str = "127.0.0.1";

/// Default HTTP listener port.
///
/// Matches `DEFAULT_HTTP_PORT` in `server-types.ts` line 107.
pub const DEFAULT_HTTP_PORT: u16 = 6335;

/// Default HTTP request path.
///
/// Matches `DEFAULT_HTTP_PATH` in `server-types.ts` line 108.
/// Follows the Streamable HTTP spec convention.
pub const DEFAULT_HTTP_PATH: &str = "/mcp";

/// Heartbeat interval in milliseconds.
///
/// Must be well under the daemon's orphan-cleanup timeout (120 s) to prevent
/// false deactivation.  30 s gives 4 missed heartbeats before deactivation.
///
/// Matches `HEARTBEAT_INTERVAL_MS` in `server-types.ts` line 11.
pub const HEARTBEAT_INTERVAL_MS: u64 = 30_000;

// ──────────────────────────────────────────────────────────────────────────────
// Transport mode
// ──────────────────────────────────────────────────────────────────────────────

/// Transport mode for the MCP server.
///
/// Mirrors the TypeScript `ServerMode` type (`server-types.ts` line 49).
///
/// - `Stdio`: MCP over stdin/stdout (default; Claude Desktop, `claude mcp …` CLI).
/// - `Http`: MCP Streamable HTTP transport.  Required for Docker deployments and
///   any client that cannot spawn a subprocess.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ServerMode {
    /// JSON-RPC over stdin/stdout (default).
    Stdio,
    /// Streamable HTTP transport (requires `--http` flag or `MCP_HTTP` env var).
    Http,
}

impl Default for ServerMode {
    fn default() -> Self {
        Self::Stdio
    }
}

