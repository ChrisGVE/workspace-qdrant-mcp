//! Core types and constants for the MCP server.
//!
//! Mirrors `src/typescript/mcp-server/src/server-types.ts`:
//! - [`ServerMode`] enum (Stdio / Http)
//! - [`HttpTlsOptions`] struct
//! - [`HttpTransportOptions`] struct
//! - [`SessionState`] struct with lifecycle helpers
//! - Server identity constants ([`SERVER_NAME`], [`SERVER_VERSION`])
//! - Default HTTP listener constants ([`DEFAULT_HTTP_HOST`], [`DEFAULT_HTTP_PORT`],
//!   [`DEFAULT_HTTP_PATH`])
//! - Timing constant ([`HEARTBEAT_INTERVAL_MS`])
//!
//! # Drop-in parity
//! `SERVER_NAME` and `SERVER_VERSION` must match the TypeScript values so that
//! MCP clients that inspect `serverInfo` behave identically against both servers
//! (RISK-18 / CC-3).

use std::path::PathBuf;
use uuid::Uuid;

// ──────────────────────────────────────────────────────────────────────────────
// Server identity constants
// ──────────────────────────────────────────────────────────────────────────────

/// MCP server name sent in the `initialize` response (`serverInfo.name`).
///
/// Must match the TypeScript server's `SERVER_NAME` constant verbatim
/// (`src/typescript/mcp-server/src/server-types.ts`, line 14).
pub const SERVER_NAME: &str = "workspace-qdrant-mcp";

/// Server version string sent in the `initialize` response (`serverInfo.version`).
///
/// Mirrors the TypeScript pattern `"0.1.0-beta1 (${BUILD_NUMBER})"`.
/// The build number is a 4-digit upper-case hex string derived from the git
/// commit count (generated by `build.rs` and injected as `BUILD_NUMBER`).
///
/// OQ-4: exact byte equality of the version string is not required for
/// client compatibility (clients use name matching, not version matching),
/// so the base `"0.1.0-beta1"` is pinned here and the build suffix appended
/// at run-time via [`server_version_string`].
pub const SERVER_VERSION_BASE: &str = "0.1.0-beta1";

/// Build number injected by `build.rs` from git commit count (4-digit hex).
pub const BUILD_NUMBER: &str = env!("BUILD_NUMBER");

/// Compute the full version string `"0.1.0-beta1 (<build>)"` at run-time.
///
/// Matches the TypeScript pattern:
/// ```typescript
/// export const SERVER_VERSION = `0.1.0-beta1 (${BUILD_NUMBER})`;
/// ```
pub fn server_version_string() -> String {
    format!("{} ({})", SERVER_VERSION_BASE, BUILD_NUMBER)
}

/// Short `--help` text for the binary.
///
/// The server takes no positional arguments; its transport is selected from
/// `MCP_SERVER_MODE` (`stdio` default | `http`). This text documents that so a
/// `--help` invocation does not silently start the stdio server.
pub fn help_text() -> String {
    format!(
        "{name} {version}\n\
         \n\
         Project-scoped Qdrant MCP server. Reaches the memexd daemon over gRPC\n\
         and reads Qdrant directly; it is a read-only client.\n\
         \n\
         USAGE:\n    {name} [--version | --help]\n\
         \n\
         The transport is selected by environment, not flags:\n\
         \x20   MCP_SERVER_MODE=stdio   JSON-RPC over stdin/stdout (default)\n\
         \x20   MCP_SERVER_MODE=http    Streamable HTTP (see MCP_HTTP_* env vars)\n",
        name = SERVER_NAME,
        version = server_version_string(),
    )
}

/// Handle the early-exit CLI flags (`--version`/`-V`, `--help`/`-h`).
///
/// Returns `Some(output)` to print and exit when such a flag is present, or
/// `None` to proceed with normal env-driven startup. This exists because the
/// binary otherwise ignores argv and immediately launches the stdio/HTTP
/// server (which blocks on stdin) — so `--version` must be intercepted here
/// rather than starting the server.
pub fn cli_early_exit<I, S>(args: I) -> Option<String>
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    for arg in args {
        match arg.as_ref() {
            "--version" | "-V" => {
                return Some(format!("{} {}", SERVER_NAME, server_version_string()));
            }
            "--help" | "-h" => return Some(help_text()),
            _ => {}
        }
    }
    None
}

