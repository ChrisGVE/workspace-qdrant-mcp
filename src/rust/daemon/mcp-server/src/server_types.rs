/// Per-session state tracked by the MCP server.
///
/// Mirrors the `SessionState` interface in `server-types.ts` lines 17–37.
/// Each MCP session has exactly one `SessionState`; it is created at
/// connection time and dropped on disconnection.
///
/// # Field mapping
///
/// | TypeScript field       | Rust field          | Notes                        |
/// |------------------------|---------------------|------------------------------|
/// | `sessionId: string`    | `session_id: Uuid`  | UUID v4                      |
/// | `projectId: string \| null`  | `project_id: Option<String>` |               |
/// | `projectPath: string \| null`| `project_path: Option<PathBuf>` |            |
/// | `watchPath: string \| null`  | `watch_path: Option<PathBuf>` |              |
/// | `isWorktree: boolean`  | `is_worktree: bool` |                              |
/// | `daemonConnected: bool`| `daemon_connected: bool` |                        |
/// | `cleaned: boolean`     | `cleaned: bool`     | idempotence flag (F-049)     |
/// | `currentBranch: string \| null` | `current_branch: Option<String>` |       |
#[derive(Debug, Clone)]
pub struct SessionState {
    /// Unique identifier for this session (UUID v4).
    pub session_id: Uuid,
    /// Project ID registered with the daemon, if any.
    pub project_id: Option<String>,
    /// Filesystem path of the project root, if registered.
    pub project_path: Option<PathBuf>,
    /// Canonical watch path returned by the daemon (may differ from
    /// `project_path` due to symlink resolution).
    pub watch_path: Option<PathBuf>,
    /// Whether the project root is a git worktree (not the main worktree).
    pub is_worktree: bool,
    /// Whether the daemon gRPC connection is currently healthy.
    pub daemon_connected: bool,
    /// Idempotence flag for `cleanup_session` (F-049).
    /// Set to `true` on the first cleanup invocation; subsequent calls no-op.
    pub cleaned: bool,
    /// Idempotence flag for `initialize_session`. Set on the first `initialize`
    /// MCP request; subsequent calls no-op. Required because the HTTP transport
    /// shares a single `SessionState` across all per-connection handlers, so
    /// `initialize` may fire more than once against the same state.
    pub initialized: bool,
    /// Current git branch detected at session start.
    /// Used as the default branch filter for search and list tools.
    /// `None` when not inside a git repository.
    pub current_branch: Option<String>,
}

impl SessionState {
    /// Create a new session with a freshly generated UUID.
    pub fn new() -> Self {
        Self {
            session_id: Uuid::new_v4(),
            project_id: None,
            project_path: None,
            watch_path: None,
            is_worktree: false,
            daemon_connected: false,
            cleaned: false,
            initialized: false,
            current_branch: None,
        }
    }

    /// Update the daemon connection flag.
    pub fn set_daemon_connected(&mut self, connected: bool) {
        self.daemon_connected = connected;
    }

    /// Register a project: set `project_id`, `project_path`, and optional
    /// `watch_path` together as an atomic operation.
    pub fn set_project(&mut self, project_id: String, path: PathBuf, watch_path: Option<PathBuf>) {
        self.project_id = Some(project_id);
        self.project_path = Some(path.clone());
        self.watch_path = watch_path.or(Some(path));
    }

    /// Clear all project-related fields (project deregistered or session reset).
    pub fn clear_project(&mut self) {
        self.project_id = None;
        self.project_path = None;
        self.watch_path = None;
        self.is_worktree = false;
        self.current_branch = None;
    }

    /// Set the current git branch.
    pub fn set_branch(&mut self, branch: String) {
        self.current_branch = Some(branch);
    }

    /// Override the canonical watch path (e.g. after symlink resolution by daemon).
    pub fn set_watch_path(&mut self, path: PathBuf) {
        self.watch_path = Some(path);
    }

    /// Whether a project has been registered with the daemon.
    pub fn is_project_registered(&self) -> bool {
        self.project_id.is_some()
    }
}

impl Default for SessionState {
    fn default() -> Self {
        Self::new()
    }
}

