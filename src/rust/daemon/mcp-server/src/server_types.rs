/// Optional TLS configuration for the HTTP transport.
///
/// Mirrors `HttpTlsOptions` in `server-types.ts` lines 59–66.
///
/// When populated the server terminates TLS itself.  Recommended production
/// pattern is to run plain HTTP behind a reverse proxy (Caddy / Traefik /
/// nginx) and leave these fields unset; native TLS exists as a fallback for
/// deployments that do not want a proxy.
#[derive(Debug, Clone)]
pub struct HttpTlsOptions {
    /// Filesystem path to the server certificate in PEM format (chain ok).
    pub cert_path: PathBuf,
    /// Filesystem path to the server private key in PEM format.
    pub key_path: PathBuf,
    /// Optional PEM path for the CA bundle, for intermediate chain serving.
    pub ca_path: Option<PathBuf>,
}

// ──────────────────────────────────────────────────────────────────────────────
// HTTP transport options
// ──────────────────────────────────────────────────────────────────────────────

/// HTTP transport configuration.
///
/// Mirrors `HttpTransportOptions` in `server-types.ts` lines 75–81.
///
/// `host` defaults to `127.0.0.1` ([`DEFAULT_HTTP_HOST`]).
/// Bind to `0.0.0.0` only inside a container.
/// `path` defaults to `/mcp` ([`DEFAULT_HTTP_PATH`]).
#[derive(Debug, Clone)]
pub struct HttpTransportOptions {
    /// Bind address for the HTTP listener.
    pub host: String,
    /// Port number for the HTTP listener.
    pub port: u16,
    /// Request path (route).
    pub path: String,
    /// Optional native TLS termination.  `None` → plain HTTP.
    pub tls: Option<HttpTlsOptions>,
}

impl Default for HttpTransportOptions {
    fn default() -> Self {
        Self {
            host: DEFAULT_HTTP_HOST.to_string(),
            port: DEFAULT_HTTP_PORT,
            path: DEFAULT_HTTP_PATH.to_string(),
            tls: None,
        }
    }
}

